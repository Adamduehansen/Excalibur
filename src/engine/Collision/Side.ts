module ex {
   /**
    * An enum that describes the sides of an Actor for collision
    * @class Side
    */
   export enum Side {
      /**
      @property None {Side}
      @static
      @final
      */
      None,
      /**
      @property Top {Side}
      @static
      @final
      */
      Top,
      /**
      @property Bottom {Side}
      @static
      @final
      */
      Bottom,
      /**
      @property Left {Side}
      @static
      @final
      */
      Left,
      /**
      @property Right {Side}
      @static
      @final
      */
      Right
   }
}